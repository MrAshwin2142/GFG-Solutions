class Solution
{
    public:
    //Function to find the first position with different bits.
    int posOfRightMostDiffBit(int m, int n)
    {
        // Your code here
         if(m == n)return -1;
        
        for(int i=1;i <= 32;i++){
            int x = m&1;
            m = m>>1;
            int y = n&1;
            n = n>>1;
            if(x^y == 1)return i;
        }
        
        return -1;
    }
};

